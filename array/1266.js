// 切比雪夫距离

/**
 * @param {number[][]} points
 * @return {number}
 */
var minTimeToVisitAllPoints = function (points) {
  let x0 = points[0][0];
  let y0 = points[0][1];
  let ans = 0;

  for (let i = 1; i < points.length; i++) {
    let x1 = points[i][0];
    let y1 = points[i][1];

    ans += Math.max(Math.abs(x0 - x1), Math.abs(y0 - y1));
    // 不使用i+ i-形式，避免还需要判断是否存在值的问题
    x0 = x1
    y0 = y1
  }

  return ans;
};

